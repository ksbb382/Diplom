def setup_heading(name, page_break=False):
    st = doc.styles[name]
    st.font.name = 'Liberation Serif'
    st.font.size = Pt(14)
    st.font.bold = True
    st.font.color.rgb = RGBColor(0, 0, 0)
    st.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
    st.paragraph_format.first_line_indent = Cm(0)
    st.paragraph_format.space_before = Pt(0)
    st.paragraph_format.space_after = Pt(12)
    st.paragraph_format.line_spacing = 1.5
    st.paragraph_format.page_break_before = page_break
    rPr = st.element.get_or_add_rPr()
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = OxmlElement('w:rFonts')
        rPr.insert(0, rFonts)
    rFonts.set(qn('w:ascii'), 'Liberation Serif')
    rFonts.set(qn('w:hAnsi'), 'Liberation Serif')
    rFonts.set(qn('w:cs'), 'Liberation Serif')

